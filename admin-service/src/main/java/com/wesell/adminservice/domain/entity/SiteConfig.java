package com.wesell.adminservice.domain.entity;

import jakarta.persistence.*;
import lombok.*;

@Entity
@NoArgsConstructor
@Getter
public class SiteConfig {

    @Id
    @Column(name = "configId")
    private final Long configId = 1L;

    @Lob
    @Column(name = "config")
    private String config;

    public SiteConfig(String config) {
        this.config = config;
    }
}
